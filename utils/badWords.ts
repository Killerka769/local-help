// Карта замены похожих символов (цифры, латиница, спецсимволы)
const charMap: Record<string, string> = {
  '0': 'о', '1': 'и', '2': 'к', '3': 'з', '4': 'ч', '5': 'н', '6': 'б', '7': 'т', '8': 'в', '9': 'д',
  'a': 'а', 'b': 'б', 'c': 'с', 'e': 'е', 'h': 'н', 'i': 'и', 'j': 'й', 'k': 'к', 'm': 'м',
  'o': 'о', 'p': 'п', 't': 'т', 'y': 'у', 'x': 'х',
  '@': 'а', '#': 'н', '$': 'с', '%': 'п', '&': 'и', '*': 'х'
}

function replaceSimilarChars(text: string): string {
  let result = ''
  for (const char of text) {
    const lower = char.toLowerCase()
    result += charMap[lower] || lower
  }
  return result
}

function compressRepeatedChars(text: string): string {
  return text.replace(/(.)\1+/g, '$1')
}

export function normalizeText(text: string): string {
  if (!text) return ''
  let normalized = text.toLowerCase()
  normalized = replaceSimilarChars(normalized)
  normalized = normalized.replace(/[^а-яё\s]/g, '')
  normalized = compressRepeatedChars(normalized)
  normalized = normalized.replace(/\s+/g, ' ').trim()
  return normalized
}

  // Функция для нормализации текста (приводим к нижнему регистру, убираем лишние символы)
  export function normalizeForCheck(text: string): string {
    return normalizeText(text)
  }
  
  // Основная функция проверки на мат
  export function containsBadWords(text: string): boolean {
    if (!text) return false
  
    const normalized = normalizeText(text)
  
    for (const badWord of forbiddenWords) {
      if (normalized.includes(badWord)) {
        return true
      }
    }
    return false
  }
  
  // Цензурирование (замена мата на звёздочки)
  export function censorBadWords(text: string): string {
    if (!text) return text

    let result = text
    const normalized = normalizeText(text)

    for (const badWord of forbiddenWords) {
      if (normalized.includes(badWord)) {
        const regex = new RegExp(badWord, 'gi')
        result = result.replace(regex, (match) => '*'.repeat(match.length))
      }
    }

    return result
  }
    
// Проверка имени (только буквы, пробелы, дефис + мат-фильтр)
export function isValidName(name: string): { valid: boolean; error?: string } {
  if (!name || name.trim().length < 2) {
    return { valid: false, error: "Имя должно содержать минимум 2 символа" }
  }
  if (name.trim().length > 50) {
    return { valid: false, error: "Имя не должно превышать 50 символов" }
  }

  const nameRegex = /^[a-zA-Zа-яА-ЯёЁ\s-]+$/
  if (!nameRegex.test(name)) {
    return { valid: false, error: "Имя может содержать только буквы, пробелы и дефис" }
  }

  if (containsBadWords(name)) {
    return { valid: false, error: "Имя содержит недопустимые слова" }
  }

  return { valid: true }
}

// Проверка текста (длина + мат)
export function isValidText(text: string, fieldName: string = "Текст"): { valid: boolean; error?: string } {
  if (!text || text.trim().length === 0) {
    return { valid: false, error: `${fieldName} не может быть пустым` }
  }
  if (text.length > 5000) {
    return { valid: false, error: `${fieldName} не должен превышать 5000 символов` }
  }
  if (containsBadWords(text)) {
    return { valid: false, error: `${fieldName} содержит недопустимые слова` }
  }
  return { valid: true }
}